// Apply dict to ALL values in the JSON, recursively
function translateObj(obj) {
  const result = {};
  for (const [key, value] of Object.entries(obj)) {
    if (typeof value === 'object' && value !== null) {
      result[key] = translateObj(value);
    } else if (typeof value === 'string') {
      result[key] = dict[value] || value;
    } else {
      result[key] = value;
    }
  }
  return result;
}

// Step 1: Apply existing path-based tr.json (keeps what's already translated)
// Step 2: Apply dictionary on top for anything still in English
const dictTranslated = translateObj(en);

// Merge: use existing tr.json translations, fall back to dict translations
function merge(existing, dictResult, original) {
  const result = {};
  for (const [key, value] of Object.entries(original)) {
    if (typeof value === 'object' && value !== null) {
      result[key] = merge(existing[key] || {}, dictResult[key] || {}, value);
    } else if (typeof value === 'string') {
      // If existing tr has a translation (differs from en), keep it
      if (existing[key] && existing[key] !== value) {
        result[key] = existing[key];
      }
      // Otherwise use dict translation
      else if (dictResult[key] && dictResult[key] !== value) {
        result[key] = dictResult[key];
      }
      // Otherwise keep original English
      else {
        result[key] = value;
      }
    } else {
      result[key] = value;
    }
  }
  return result;
}

const merged = merge(tr, dictTranslated, en);
fs.writeFileSync('apps/mobile/src/i18n/tr.json', JSON.stringify(merged, null, 2), 'utf8');

// Count
let total = 0, translated = 0, filterData = 0;
function count(enObj, trObj, path = '') {
  for (const [key, value] of Object.entries(enObj)) {
    const p = path ? path + '.' + key : key;
    if (typeof value === 'object') count(value, trObj[key] || {}, p);
    else if (typeof value === 'string') {
      total++;
      const isFilter = p.startsWith('settings.') && !value.includes(' ') && value.length <= 20 && /^[A-Z][a-z]+$/.test(value);
      if (isFilter) filterData++;
      else if (trObj[key] !== value) translated++;
    }
  }
}
