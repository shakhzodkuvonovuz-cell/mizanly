// Apply translations to the tr.json
function setPath(obj, path, value) {
  const parts = path.split('.');
  let current = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    if (!current[parts[i]]) current[parts[i]] = {};
    current = current[parts[i]];
  }
  current[parts[parts.length - 1]] = value;
}

let translated = 0, kept = 0, added = 0;
for (const item of remaining) {
  if (keepEnglish.has(item.value)) {
    kept++; // Intentionally keep in English
    continue;
  }
  if (translations[item.value]) {
    setPath(tr, item.path, translations[item.value]);
    translated++;
  }
}

