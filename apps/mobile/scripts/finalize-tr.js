// Recount
let total = 0, done = 0, filterData = 0;
function count(enObj, trObj, path = '') {
  for (const [key, value] of Object.entries(enObj)) {
    const p = path ? path + '.' + key : key;
    if (typeof value === 'object') count(value, trObj[key] || {}, p);
    else if (typeof value === 'string') {
      total++;
      const isFilter = p.startsWith('settings.') && !value.includes(' ') && value.length <= 20 && /^[A-Z][a-z]+$/.test(value);
      if (isFilter) filterData++;
      else if (trObj[key] !== value) done++;
    }
  }
}
